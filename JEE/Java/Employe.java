public class Employe{
    private String nom, prenom;
    private double salaire;

    // Constructeurs
    public Employe() {}

    public Employe(String n, String p){
        nom = n;
        prenom = p;
    }

    public Employe(String n, String p, double s){
        nom = n;
        prenom = p;
        salaire = s;
    }
}





